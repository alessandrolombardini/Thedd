package thedd.view;

/**
 * Enum descibing possible application status. 
 */
public enum ApplicationViewState {

    /**
     * Menu state.
     */
    MENU, 

    /**
     * State where the user set values of new game session. 
     */
    NEW_GAME, 

    /**
     * Game state.
     */
    GAME,

    /**
     * Game over state.
     */
    GAME_OVER;
}
