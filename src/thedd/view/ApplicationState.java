package thedd.view;

/**
 * Enum descibing possible application status. 
 */
public enum ApplicationState {

    /**
     * Menu state.
     */
    MENU, 

    /**
     * Settings state. 
     */
    SETTINGS, 

    /**
     * State where the user set values of new game session. 
     */
    NEW_GAME, 

    /**
     * Game state.
     */
    GAME;
}
