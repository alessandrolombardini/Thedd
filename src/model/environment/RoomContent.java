package model.environment;

/**
 * Representation of objects that could be inside a normal room.
 * 
 */
public enum RoomContent {

    /**
    * Enemy that the main player have to fight.
    */
    ENEMY,

    /**
     * Treasure that the main player can collect.
     */
    TREASURE,

    /**
     * Contraption that the main player can meet.
     */
    CONTRAPTION
}
